<#
.SYNOPSIS
Generates CoreWCF service contracts, MessageContracts, partial implementations and the
route table from the converted ASMX service classes, preserving the ASMX wire format
(wrapped doc/literal, {ns}/{method} SOAPAction, TripXML SoapHeader, {method}Result naming).

.PARAMETER HostRoot
Path to the wsTripXML-converted repo.

.PARAMETER Only
Optional list of service class short names to generate (e.g. wsPing,wsAirAvail).
When omitted, generates for every .asmx with a resolvable code-behind class.
#>
param(
    [string]$HostRoot = (Join-Path $PSScriptRoot '..\src\wsTripXML'),
    [string[]]$Only = @()
)

$ErrorActionPreference = 'Stop'
$HostRoot = (Resolve-Path $HostRoot).Path
$outDir = Join-Path $HostRoot 'Code\Generated'
New-Item -ItemType Directory -Force $outDir | Out-Null

# ---- collect .asmx routes -> class names -------------------------------------------------
$asmxFiles = @(Get-ChildItem $HostRoot -Filter *.asmx -File) +
             @(Get-ChildItem (Join-Path $HostRoot 'App_Data') -Filter *.asmx -Recurse -File -ErrorAction SilentlyContinue)

$routes = @()
foreach ($asmx in $asmxFiles) {
    $markup = Get-Content $asmx.FullName -Raw
    if ($markup -notmatch 'Class="(?<cls>[\w.]+)"') { Write-Warning "no Class= in $($asmx.Name)"; continue }
    $fullClass = $Matches['cls']
    $shortClass = $fullClass.Split('.')[-1]
    $rel = $asmx.FullName.Substring($HostRoot.Length).TrimStart('\')
    $route = if ($rel -like 'App_Data\Admin\*') { "/Admin/$($asmx.Name)" } else { "/$($asmx.Name)" }
    $routes += [pscustomobject]@{ Route = $route; FullClass = $fullClass; ShortClass = $shortClass }
}

# ---- map class -> code-behind file -------------------------------------------------------
# (StoredFare services use plain .cs code-behind names)
$codeFiles = @(Get-ChildItem (Join-Path $HostRoot 'Code') -Filter *.asmx.cs -Recurse -File) +
             @(Get-ChildItem $HostRoot -Filter *.asmx.cs -File) +
             @(Get-ChildItem (Join-Path $HostRoot 'Code') -Filter 'wsStoredFare*.cs' -File)
$classToFile = @{}
foreach ($f in $codeFiles) {
    $raw = Get-Content $f.FullName -Raw
    foreach ($m in [regex]::Matches($raw, 'public\s+(?:partial\s+)?class\s+(\w+)')) {
        if (-not $classToFile.ContainsKey($m.Groups[1].Value)) { $classToFile[$m.Groups[1].Value] = $f.FullName }
    }
}

$routeEntries = New-Object System.Collections.Generic.List[string]
$generated = 0; $skipped = @()

foreach ($svc in $routes) {
    $cls = $svc.ShortClass
    if ($Only.Count -gt 0 -and $Only -notcontains $cls) { continue }
    if (-not $classToFile.ContainsKey($cls)) { $skipped += "$cls (no code-behind)"; continue }

    $raw = Get-Content $classToFile[$cls] -Raw
    $csNamespace = [regex]::Match($raw, 'namespace\s+([\w.]+)').Groups[1].Value
    $nsUri = [regex]::Match($raw, '\[WebService\([^\]]*Namespace\s*=\s*"(?<ns>[^"]+)"').Groups['ns'].Value
    if (-not $nsUri) { $nsUri = 'http://tempuri.org/' }
    $svcName = [regex]::Match($raw, '\[WebService\([^\]]*Name\s*=\s*"(?<n>[^"]+)"').Groups['n'].Value
    if (-not $svcName) { $svcName = $cls }

    # ---- parse [WebMethod] operations ----------------------------------------------------
    $lines = $raw -split "`r?`n"
    $ops = @()
    for ($i = 0; $i -lt $lines.Count; $i++) {
        if ($lines[$i] -notmatch '^\s*\[WebMethod') { continue }
        $hasHeader = $false; $headerField = 'tXML'
        for ($j = $i + 1; $j -lt [Math]::Min($i + 8, $lines.Count); $j++) {
            if ($lines[$j] -match 'SoapHeader\("(?<hf>\w+)"') { $hasHeader = $true; $headerField = $Matches['hf']; continue }
            if ($lines[$j] -match '^\s*\[') { continue }  # other attributes (ScriptMethod etc.)
            if ($lines[$j] -match '^\s*public\s+(?<ret>[\w.]+)\s+(?<name>\w+)\((?:(?<ptype>[\w.]+)\s+(?<pname>\w+))?\)') {
                $ops += [pscustomobject]@{
                    Return = $Matches['ret']; Name = $Matches['name']
                    ParamType = $Matches['ptype']; ParamName = $Matches['pname']
                    HasHeader = $hasHeader; HeaderField = $headerField
                }
            }
            break
        }
    }
    if ($ops.Count -eq 0) {
        # Already-converted services have no [WebMethod] left to parse; keep their route
        # alive if a previously generated contract exists.
        if (Test-Path (Join-Path $outDir "$cls.g.cs")) {
            $routeEntries.Add("            new(typeof($csNamespace.$cls), typeof($csNamespace.I$cls), `"$($svc.Route)`"),")
            $skipped += "$cls (already converted; route kept)"
        } else {
            $skipped += "$cls (no WebMethods)"
        }
        continue
    }

    # ---- emit ------------------------------------------------------------------------------
    $sb = New-Object System.Text.StringBuilder
    [void]$sb.AppendLine('// <auto-generated>')
    [void]$sb.AppendLine('// Generated by tools/Generate-Contracts.ps1 - do not edit by hand.')
    [void]$sb.AppendLine("// Source: $cls ($($svc.Route)), namespace $nsUri")
    [void]$sb.AppendLine('// </auto-generated>')
    [void]$sb.AppendLine('using CoreWCF;')
    [void]$sb.AppendLine()
    [void]$sb.AppendLine("namespace $csNamespace")
    [void]$sb.AppendLine('{')

    # contract interface
    [void]$sb.AppendLine("    [ServiceContract(Namespace = `"$nsUri`", Name = `"$svcName`")]")
    [void]$sb.AppendLine('    [XmlSerializerFormat(Style = OperationFormatStyle.Document, Use = OperationFormatUse.Literal)]')
    [void]$sb.AppendLine("    public interface I$cls")
    [void]$sb.AppendLine('    {')
    foreach ($op in $ops) {
        $action = "$nsUri/$($op.Name)"
        [void]$sb.AppendLine("        [OperationContract(Action = `"$action`")]")
        if ($op.HasHeader) {
            [void]$sb.AppendLine("        ${cls}_$($op.Name)Response $($op.Name)(${cls}_$($op.Name)Request request);")
        } elseif ($op.ParamType) {
            [void]$sb.AppendLine("        $($op.Return) $($op.Name)($($op.ParamType) $($op.ParamName));")
        } else {
            [void]$sb.AppendLine("        $($op.Return) $($op.Name)();")
        }
        [void]$sb.AppendLine()
    }
    [void]$sb.AppendLine('    }')
    [void]$sb.AppendLine()

    # message contracts for headered ops
    foreach ($op in $ops | Where-Object HasHeader) {
        [void]$sb.AppendLine("    [MessageContract(WrapperName = `"$($op.Name)`", WrapperNamespace = `"$nsUri`", IsWrapped = true)]")
        [void]$sb.AppendLine("    public class ${cls}_$($op.Name)Request")
        [void]$sb.AppendLine('    {')
        [void]$sb.AppendLine("        [MessageHeader(Namespace = `"$nsUri`")]")
        [void]$sb.AppendLine('        public TripXML TripXML;')
        if ($op.ParamType) {
            [void]$sb.AppendLine()
            [void]$sb.AppendLine("        [MessageBodyMember(Namespace = `"$nsUri`", Order = 0)]")
            [void]$sb.AppendLine("        public $($op.ParamType) $($op.ParamName);")
        }
        [void]$sb.AppendLine('    }')
        [void]$sb.AppendLine()
        [void]$sb.AppendLine("    [MessageContract(WrapperName = `"$($op.Name)Response`", WrapperNamespace = `"$nsUri`", IsWrapped = true)]")
        [void]$sb.AppendLine("    public class ${cls}_$($op.Name)Response")
        [void]$sb.AppendLine('    {')
        [void]$sb.AppendLine("        [MessageBodyMember(Namespace = `"$nsUri`", Order = 0)]")
        [void]$sb.AppendLine("        public $($op.Return) $($op.Name)Result;")
        [void]$sb.AppendLine('    }')
        [void]$sb.AppendLine()
    }

    # partial implementation: wires the contract to the converted code-behind
    [void]$sb.AppendLine('    [ServiceBehavior(InstanceContextMode = InstanceContextMode.PerCall)]')
    [void]$sb.AppendLine("    public partial class $cls : I$cls")
    [void]$sb.AppendLine('    {')
    $needsBody = $false
    foreach ($op in $ops | Where-Object HasHeader) {
        $needsBody = $true
        [void]$sb.AppendLine("        public ${cls}_$($op.Name)Response $($op.Name)(${cls}_$($op.Name)Request request)")
        [void]$sb.AppendLine('        {')
        [void]$sb.AppendLine("            $($op.HeaderField) = request.TripXML;")
        if ($op.ParamType) {
            [void]$sb.AppendLine("            return new ${cls}_$($op.Name)Response { $($op.Name)Result = $($op.Name)(request.$($op.ParamName)) };")
        } else {
            [void]$sb.AppendLine("            return new ${cls}_$($op.Name)Response { $($op.Name)Result = $($op.Name)() };")
        }
        [void]$sb.AppendLine('        }')
        [void]$sb.AppendLine()
    }
    if (-not $needsBody) {
        [void]$sb.AppendLine('        // all operations are plain (no SoapHeader) and implemented by the code-behind directly')
    }
    [void]$sb.AppendLine('    }')
    [void]$sb.AppendLine('}')

    Set-Content -Path (Join-Path $outDir "$cls.g.cs") -Value $sb.ToString() -Encoding UTF8
    $routeEntries.Add("            new(typeof($csNamespace.$cls), typeof($csNamespace.I$cls), `"$($svc.Route)`"),")
    $generated++
}

# ---- route table -------------------------------------------------------------------------
$routesSb = New-Object System.Text.StringBuilder
[void]$routesSb.AppendLine('// <auto-generated>')
[void]$routesSb.AppendLine('// Generated by tools/Generate-Contracts.ps1 - do not edit by hand.')
[void]$routesSb.AppendLine('// </auto-generated>')
[void]$routesSb.AppendLine('using System;')
[void]$routesSb.AppendLine()
[void]$routesSb.AppendLine('namespace wsTripXML.Hosting')
[void]$routesSb.AppendLine('{')
[void]$routesSb.AppendLine('    public static class ServiceRoutes')
[void]$routesSb.AppendLine('    {')
[void]$routesSb.AppendLine('        public sealed record Entry(Type Implementation, Type Contract, string Route);')
[void]$routesSb.AppendLine()
[void]$routesSb.AppendLine('        public static readonly Entry[] All =')
[void]$routesSb.AppendLine('        {')
$routeEntries | ForEach-Object { [void]$routesSb.AppendLine($_) }
[void]$routesSb.AppendLine('        };')
[void]$routesSb.AppendLine('    }')
[void]$routesSb.AppendLine('}')
Set-Content -Path (Join-Path $outDir 'ServiceRoutes.g.cs') -Value $routesSb.ToString() -Encoding UTF8

Write-Host "generated contracts for $generated services -> $outDir"
if ($skipped.Count) { Write-Host "skipped:"; $skipped | ForEach-Object { Write-Host "  $_" } }
